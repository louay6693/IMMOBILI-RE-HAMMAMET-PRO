// ─────────────────────────────────────────────
//  i18n Engine
// ─────────────────────────────────────────────
(function () {
  const STORAGE_KEY = 'hammamet_lang';
  let currentLang = localStorage.getItem(STORAGE_KEY) || 'fr';

  /**
   * Apply translations to all [data-i18n] elements.
   * Supports: data-i18n="key"           → element.innerHTML = t(key)
   *           data-i18n-html="key"      → same, explicit innerHTML
   *           data-i18n-placeholder="key" → element.placeholder = t(key)
   */
  function applyTranslations(lang) {
    const t = TRANSLATIONS[lang] || TRANSLATIONS['fr'];

    // innerHTML / textContent elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // explicit HTML (same as above but makes intent clear)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    // RTL support for Arabic
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);

    // Update active button styling
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
  }

  // Public: called by onclick="setLang(this)"
  window.setLang = function (btn) {
    const lang = btn.getAttribute('data-lang');
    applyTranslations(lang);
  };

  // Run on page load
  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
  });
})();